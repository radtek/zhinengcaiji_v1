package parser.eric.pm;

public class EricssonPmParserException extends Exception {

	private static final long serialVersionUID = 1521067898269755465L;

	public EricssonPmParserException() {
		super();
	}

	public EricssonPmParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public EricssonPmParserException(String message) {
		super(message);
	}

	public EricssonPmParserException(Throwable cause) {
		super(cause);
	}

}
